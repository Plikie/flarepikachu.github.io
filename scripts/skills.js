

function popup1() {
    var x = document.getElementById("popup-1");
    var dimple = document.getElementById("dim")
    if (x.style.display === "block") {
      x.style.display = "none";
      dimple.classList.toggle('dimmer-active')
    } else {
      x.style.display = "block";
      dimple.classList.toggle('dimmer-active')
    }
  }

